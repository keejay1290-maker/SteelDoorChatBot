def send_customer_quote_email(
    *,
    customer_email: str,
    customer_name: str | None,
    quote: "QuoteResponse",
) -> bool:
    """Send the quote summary to the customer's own email address. Returns True on success."""
    smtp_host = os.environ.get("SMTP_HOST", "").strip()
    smtp_user = os.environ.get("SMTP_USER", "").strip()
    smtp_pass = os.environ.get("SMTP_PASS", "").strip()

    if not (smtp_host and smtp_user and smtp_pass):
        logger.debug("SMTP not configured — customer quote email skipped for %s", quote.reference)
        return False

    smtp_port = int(os.environ.get("SMTP_PORT", "587"))
    from_addr = os.environ.get("EMAIL_FROM", smtp_user).strip()

    name_part = customer_name or "there"
    subject = f"Your Steel Door Estimate — {quote.reference}"

    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"] = from_addr
    msg["To"] = customer_email

    lines_txt = "\n".join(f"  {ln.label}: £{ln.amount:,.2f}" for ln in quote.lines)
    if quote.sale_discount:
        lines_txt += f"\n  Summer Sale discount: -£{quote.sale_discount:,.2f}"

    plain = (
        f"Hi {name_part},\n\n"
        f"Thanks for chatting with the Steel Door Company AI consultant. "
        f"Here's your indicative estimate:\n\n"
        f"Reference: {quote.reference}\n"
        f"Product:   {quote.product_name}\n"
        f"Quantity:  {quote.quantity}\n\n"
        f"Price breakdown:\n{lines_txt}\n\n"
        f"  Subtotal (exc. VAT): £{quote.subtotal:,.2f}\n"
        f"  VAT (20%):           £{quote.vat:,.2f}\n"
        f"  TOTAL INC. VAT:      £{quote.total:,.2f}\n\n"
        f"Lead time: {quote.lead_time}\n\n"
        f"This is an indicative estimate only, subject to a free site survey. "
        f"A member of our team will be in touch shortly to confirm and arrange your survey.\n\n"
        f"For anything urgent:\n"
        f"  Phone: 01785 526016\n"
        f"  Email: sales@steeldoorcompany.co.uk\n"
        f"  Mon-Fri 8am-6pm | Sat 9am-2pm\n\n"
        f"Kind regards,\nSteel Door Company\nUnit C, Scarlet Court, Stafford ST16 1YJ\n"
        f"---\nAuto-generated by Steel Door AI Consultant\n"
    )
    msg.attach(MIMEText(plain, "plain"))

    try:
        ctx = ssl.create_default_context()
        with smtplib.SMTP(smtp_host, smtp_port, timeout=10) as server:
            server.ehlo()
            server.starttls(context=ctx)
            server.login(smtp_user, smtp_pass)
            server.sendmail(from_addr, [customer_email], msg.as_string())
        logger.info("Customer quote email sent for %s", quote.reference)
        return True
    except Exception as exc:
        logger.warning("Customer quote email failed for %s: %s", quote.reference, exc)
        return False
